package day18_NestedLoop;

import java.util.Scanner;

public class AdditionOfTwoNumbers {
    public static void main(String[] args) {

        for (int i = 1; i < 10; i++) {
           
            for (int j = 0; j < i; j++) {
                System.out.print("* ");
            }
            System.out.println();

        }





    }
}
