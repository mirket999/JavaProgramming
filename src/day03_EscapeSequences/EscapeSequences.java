package day03_EscapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Java \nPython \nC#");
        System.out.println("-------------------------------");
        System.out.println("\nHello Everyone,\nHow are you all today?\nToday we will talk about escape sequences");
        System.out.println("-------------------------------");
        System.out.println("Hello Everyone,\n\tHow are you all today?\n\tToday we will talk about escape sequences");
        System.out.println("-------------------------------");
        System.out.println("Hello \"Everyone\",\\How are you all today?\\Today we will talk about escape sequences");


    }

}

/*
\n: starts a new line
\t: paragraph space (tab)
\\: gives single backslash on the console
\": adds quote
 */
